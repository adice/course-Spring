package com.bean;

public class Cup {
	private Integer cupId;
	private Integer cupPrice;
	private String cupBrand;
	public Integer getCupId() {
		return cupId;
	}
	public void setCupId(Integer cupId) {
		this.cupId = cupId;
	}
	public Integer getCupPrice() {
		return cupPrice;
	}
	public void setCupPrice(Integer cupPrice) {
		this.cupPrice = cupPrice;
	}
	public String getCupBrand() {
		return cupBrand;
	}
	public void setCupBrand(String cupBrand) {
		this.cupBrand = cupBrand;
	}
	
}
